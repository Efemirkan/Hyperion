# Initialize Variable
str = "The!quick!brown!fox!jumps!over!the!lazy!dog."  # To store sentence

# Replace "!" by whitespace and print to display
str1 = str.replace("!", " ")
print(str1)

# Capitalise every letter in string and print to display
str2 = str1.upper()
print(str2)

# Reverse sentence and print to display
str3 = str2[::-1]
print(str3)