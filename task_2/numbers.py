# Take user's inputs and case to integer
num1 = int(input("Please enter first different integers: "))
num2 = int(input("Please enter second different integers: "))
num3 = int(input("Please enter third different integers: "))

# Calculate total of numbers and print to display
print(num1 + num2 + num3)

# Calculate first integer minus second integer and print to display 
print(num1 - num2)

# Calculate third number multiply by first number and print to display
print(num3 * num1)

# Calculate total of numbers then divide by third number and print to display
print((num1 + num2 + num3) / num3)
